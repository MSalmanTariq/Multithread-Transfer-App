    private void recvbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recvbtnActionPerformed
      
        if(!folderSelected){
            JOptionPane.showMessageDialog(this,
    "Select Destination first",
    "Error",
    JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(intPort.getText().isEmpty()){
        JOptionPane.showMessageDialog(this,
    "Provide Initial Port",
    "Error",
    JOptionPane.ERROR_MESSAGE);
        return;
    }
        int iniport = Integer.parseInt(intPort.getText());
        
        progbar.setVisible(true);
        progbar.setIndeterminate(true);
        logtxt.setText("Waiting for Client ...");
        recvbtn.setEnabled(false);
   
        new java.util.Timer().schedule( 
        new java.util.TimerTask() {
            @Override
            public void run() {
                try {
                    
                    mr.connect(iniport,reciever.this,path);
                } catch (IOException ex) {
                   JOptionPane.showMessageDialog(reciever.this,
    "Error in Recieving File\n"+ex.getLocalizedMessage()+"\nUse RESET button to try again",
    "Error",
    
    JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(reciever.class.getName()).log(Level.SEVERE, null, ex);
                } catch (InterruptedException ex) {
                     JOptionPane.showMessageDialog(reciever.this,
    "Error in Recieving File\n"+ex.getLocalizedMessage()+"\nUse RESET button to try again",
    "Error",
    
    JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(reciever.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }, 
        2000 
);
       
        
        // TODO add your handling code here:
    
    }//GEN-LAST:event_recvbtnActionPerformed
    
