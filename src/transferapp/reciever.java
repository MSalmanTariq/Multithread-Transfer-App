    public void getFileData(String filename,String extension, long size, int thread){
     this.filename=filename;
    this.fileext=extension;
    this.size=size;
    long fileSizeInBytes = this.size;
       
// Convert the bytes to Kilobytes (1 KB = 1024 Bytes)
double fileSizeInKB = fileSizeInBytes / 1024;
// Convert the KB to MegaBytes (1 MB = 1024 KBytes)
double fileSizeInMB = fileSizeInKB / 1024;
    this.thread=thread;
    
      this.progbar.setVisible(true);
      this.progbar.setIndeterminate(false);
        this.progbar.setValue(2);
        this.progbar.setStringPainted(true);
        
       this. logtxt.setText("Recieving ...");
       this. resetbtn.setEnabled(false);
         this. fn.setText(this.filename);
      this.  fe.setText(this.fileext);
      this.  fs.setText(Double.toString(fileSizeInMB));
      this.  th.setText(Integer.toString(this.thread));
       
    
    }
